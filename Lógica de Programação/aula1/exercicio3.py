n1 = float(input("Digite o primeiro número: "))
n2 = float(input("Digite o segundo número: "))
soma = (n1 + n2)
subtrair = (n1 - n2)
dividir = (n1 / n2)
multiplicar = (n1 * n2)
print(n1, "+", n2, "=", soma)
print(n1, "-", n2, "=", subtrair)
print(n1, "/", n2, "=", dividir)
print(n1, "*", n2, "=", multiplicar)
